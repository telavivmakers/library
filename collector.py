def collect():
    barcodes = []
    with open('barcode_scan.log', 'a') as f:
        while True:
            sys.stdout.write('>> ')
            scanned = raw_input()
            barcodes.append(scanned)
            f.write(scanned)
            f.write('\n')

if __name__ == '__main__':
    init()
    collect()
